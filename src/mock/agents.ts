const buildAgents = (): Agent[] => {
  const result: Agent[] = [...starAgents];
  ledgerSeed.forEach((item, idx) => {
    result.push({
      ...item,
      modelSource: pickModel(item.type, idx),
    });
  });
  return result;
};

export const mockAgents = buildAgents();

export const getAgentById = (id: string): Agent | undefined => {
  return mockAgents.find((a) => a.id === id);
};

export const getAgentsByType = (type: AgentType): Agent[] => {
  return mockAgents.filter((a) => a.type === type);
};

export const getAgentsByStatus = (status: LifecycleStatus): Agent[] => {
  return mockAgents.filter((a) => a.lifecycleStatus === status);
};

export const getAgentsByDepartment = (department: string): Agent[] => {
  return mockAgents.filter((a) => a.department === department);
};

export const getOnlineAgents = (): Agent[] => {
  return mockAgents.filter((a) => a.runStatus === '在线');
};

