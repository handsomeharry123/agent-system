/**
 * 报告页 V3.4 - 演示数据
 *
 * 与两份 docx 模板 1:1 对齐：
 *   - 全院：全院智能体运行管理情况报告模板
 *   - 科室：科室智能体运行情况报告模板-以放射科为例
 *
 * 数据组织：
 *   - buildPlatformReport()   信息科管理员版本
 *   - buildDeptReport(dept)    科室管理员版本（默认放射科）
 *
 * 节点类型：cover / toc / h2 / h3 / p / kpi / chart / table / matrix / colophon
 */

export type SectionType =
  | 'cover'
  | 'toc'
  | 'title'
  | 'h2'
  | 'h3'
  | 'p'
  | 'kpi'
  | 'chart'
  | 'table'
  | 'matrix'
  | 'colophon';

export interface ReportNode {
  id: string;
  type: SectionType;
  text?: string;
  kpis?: Array<{ label: string; value: string; unit?: string; color?: string }>;
  chart?: {
    title: string;
    chartType: 'bar' | 'pie' | 'line';
    data: Array<{ name: string; value: number }>;
  };
  table?: {
    title: string;
    headers: string[];
    rows: Array<{ key: string; cells: string[] }>;
  };
  matrix?: {
    title: string;
    rows: string[];
    cols: string[];
    data: number[][];
    legend?: string;
  };
  cover?: {
    hospital: string;
    reportTitle: string;
    deptName: string;
    period: string;
    generatedBy: string;
    reportDate: string;
    templateNote: string;
  };
  toc?: { items: Array<{ anchor: string; label: string }> };
  colophon?: { generator: string; reportDate: string; note: string };
}

